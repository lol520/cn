		internal class ActiveItem
		{
			public int Id;
			public List<Utility.Map.MapType> Maplist;
			public string Name;
			public ItemType TypeItem;
			public MenuType TypeMenu;

			public ActiveItem(string name, int id, List<Utility.Map.MapType> maplist, ItemType typeitem, MenuType typemenu)
			{
				Name = name;
				Id = id;
				Maplist = maplist;
				TypeItem = typeitem;
				TypeMenu = typemenu;
			}

			public bool IsActive()
			{
				return Maplist.Contains(Utility.Map.GetMap()._MapType);
			}

			public bool IsEnabled()
			{
				if (TypeItem == ItemType.Cleanse)
					return PUC.Menu.Item("act_item_" + Id + "_use").GetValue<bool>();
				if (TypeItem == ItemType.Offensive)
				{
					if (Orbwalker.CurrentMode == Orbwalker.Mode.Combo)
						return PUC.Menu.Item("act_item_" + Id + "_useCombo").GetValue<bool>();
					if (Orbwalker.CurrentMode == Orbwalker.Mode.Harass)
						return PUC.Menu.Item("act_item_" + Id + "_useHarass").GetValue<bool>();
				}
				if(TypeItem == ItemType.OnHit )
				{
					if(Orbwalker.CurrentMode == Orbwalker.Mode.Combo)
						return PUC.Menu.Item("act_item_" + Id + "_useCombo").GetValue<bool>();
					if(Orbwalker.CurrentMode == Orbwalker.Mode.Harass)
						return PUC.Menu.Item("act_item_" + Id + "_useHarass").GetValue<bool>();
					if(Orbwalker.CurrentMode == Orbwalker.Mode.LaneClear)
						return PUC.Menu.Item("act_item_" + Id + "_useLaneClear").GetValue<bool>();	
				}

				return false;
			}

			public bool IsInInventory()
			{
				return Items.HasItem(Id);
			}

			public bool IsReady()
			{
				return Items.CanUseItem(Id);
			}

			public bool IsonCooldown()
			{
				foreach(var slot in ObjectManager.Player.InventoryItems.Where(slot => slot.Id == (ItemId)Id))
				{Chat.Print( slot.Charges.ToString()) ;
				Chat.Print(slot.Stacks.ToString());
					return true;
				}
				return true;
			}

			public void CastItem()
			{
				Items.UseItem(Id);
			}

			public void CastItem(Obj_AI_Base target)
			{
				Items.UseItem(Id, target);
			}

			public void CastItem(Vector3 position)
			{
				Items.UseItem(Id, position);
			}

